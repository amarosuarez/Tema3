package sejercicio02;

public class Media {

	public static double media(int num1, int num2) {
		double media;
		
		media = (double) (num1+num2)/2;
		
		return media;
	}
	
	public static double media(double num1, double num2, double num3) {
		double media;
		
		media = (num1+num2+num3)/3;
		
		return media;
	}
	
}
